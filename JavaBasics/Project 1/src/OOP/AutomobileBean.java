package OOP;

public class AutomobileBean {
private String Color;
private int Speed;
private String Make;

public String getColor() {
	return Color;
}
public void setColor(String color) {
	Color = color;
}
public String getMake() {
	return Make;
}
public void setMake(String make) {
	Make = make;
}

public void setSpeed (int speed) {
   this.Speed = speed;
}

public int getSpeed() {
	return Speed;
}

}
