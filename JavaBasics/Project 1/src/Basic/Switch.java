package Basic;

public class Switch {
	public static void main(String[] args) {
		int number = 50;
		switch (number) {
		case 1: System.out.println("1");
		break;
		case 25: System.out.println("25");
		break;
		case 50: System.out.println("50");
		break;
		default: System.out.println("Not in 1, 25 or 50");
		}
		
		
		
	}

}
